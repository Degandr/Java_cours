package Home_work_10;

public class Calc {
    public int sum(int a, int b) {
        return a + b;
    }

    public int min(int a, int b) {
        return a - b;
    }

    public int mult(int a, int b) {
        return a * b;
    }

    public int dev(int a, int b) {
        return a / b;
    }
}
